#include <iostream>
using namespace std;
int main()
{
	short int Num1;
	cout << "Please Enter The First Number : ";
	cin >> Num1;

	short int Num2;
	cout << "Please Enter The Second Number : ";
	cin >> Num2;

	cout << Num1 << " + " << Num2 << " = " << Num1 + Num2 << endl;
	cout << Num1 << " - " << Num2 << " = " << Num1 - Num2 << endl;
	cout << Num1 << " / " << Num2 << " = " << Num1 / Num2 << endl;
	cout << Num1 << " * " << Num2 << " = " << Num1 * Num2 << endl;
	cout << Num1 << " % " << Num2 << " = " << Num1 % Num2 << endl;

}